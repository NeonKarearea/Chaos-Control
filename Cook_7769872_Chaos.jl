md"""
### Background

Chaos Theory is a field of scientific study that is focused on dynamical systems that are highly sensitive to initial conditions.

In the textbook Nonlinear Dynamics and Chaos by Steven H.Strogatz$^{3}$, he states that there is yet a universally accepted definition of the term chaos, however a good definition that most agree upon is, "Chaos is aperiodic long-term behaviour in a deterministic system that exhibits sensitive dependence on initial conditions."

All these definitions are great but, how did chaos arise and what is the history behind its discovery?

It all started back in the 1880's while Henri Poincaré was studying the classical mechanics three body problem. During his studies he found that there were cases where the orbits were nonperiodic however, neither forever increasing nor approaching a fixed point. Later in 1898 Jacques Hadamard published a study on chaotic motion that found that particle trajectories would diverge exponentially away from one another if the particles had a positive Lyapunov exponent.

It then wasn't really until the mid-twentieth century where chaos theory became more formalised. Before chaos theory the leading system theory at the time was linear theory which failed to explain the observations of the seemingly simple system of the logistic map. At a similar time, we had the introduction to the electronic computer. This was very convenient for mathematicians and physicists at the time, where now they could perform repeated iterations of simple mathematical formulas, which without the computer would be long-some and impractical.

With computers being available it was now possible to numerically experiment with nonlinear systems to get a better intuition about them. Experiments like these enabled Lorenz to discover chaotic motion on a strange attractor in 1963. Lorenz was studying a simplified model of unpredictable weather patterns. He found that not only did his solutions never reach an equilibrium but they oscillated irregularly and were aperiodic. He also importantly found that starting conditions close but not exact would end up in completely different trajectories.

So what is chaos theory used for our modern day world? As discussed before it has been very useful for observing weather patterns and predicting weather motions in the near future. From a Barishal University Journal$^{6}$, chaos theory is also used in parts of mathematics, geology, microbiology, biology, computer science, economics, engineering, finance, algorithmic trading, meteorology, philosophy, anthropology, physics, politics, population dynamics, physiology, and robotics.  One important application of Chaos Theory described in this journal was chaos in the human body. One example is the chaotic behaviour of an epileptic brain seizure. From this Garfinkel, Spano, Ditto and Weiss (1992) developed a method they called proportional perturbation feedback (PPF) which makes a minimal intervention to move the system back onto a stable pathway. This application  became very useful as it was less intrusive on the patient's system than the traditional periodic method which was never successful in restoring a periodic rhythm.

"""

# ╔═╡ 23aaabfe-8d71-474d-85ce-460c923c9a61
md"""
### Theory

All of this brings us to the unintimidating system, the double pendulum. In the Research Project; An Introduction to the Double Pendulum by Joe Chen$^{2}$, he stated that "It wasn't until the advent of powerful processors and efficient computers that systems such as the double pendulum can be studied in more detail since the solutions to its governing equations in most cases can only be solved numerically. The discovery of rich and complicated dynamics once these equations are solved shows the chaotic nature of the double pendulum; where its apparently deterministic motions are actually unpredictable." This is to say that Chaos can arrive from simple systems!

One key concept of Chaos is the Liapunov Exponent$^{5}$.

!!! info "Liapunov Exponent"

	$\sigma=\lim_{n\to \infty}\frac{1}{n}\left\{\sum_{i=0}^{n-1}\ln\big|f'(x_i)\big|\right\}$

The Liapunov Exponent is key to understanding how infinitesimally close trajectories separate, and at what rate they do so. When studying our system of the double pendulum, we can evaluate the Liapunov Exponent and this will tell us if our initial conditions will make the system behave regular (for $\sigma < 0$) or chaotic (for $\sigma >0$). 

Another key concept of Chaos are attractors. Let us take a look at a simpler system, the Lorenz Attractor. 

>$dx = \sigma (y-x)$
>$dy = x(\rho -z) - y$
>$dz = xy - \beta z$

When solving this system, running it over a time period, we notice that paths tend to oscillate aperiodic around two fixed points. These points seem to "attract" paths back towards them causing bounded chaos, where solutions are fixed within a region yet still fall into chaotic motion. These systems are a group of objects called attractors. Specifically, this system is a strange attractor$^{4}$ which is a dynamical attractor with a fractal structure.

This system will be very relevant for our double pendulum, as our system is bound by the rods holding each weight as well as the energy being conserved. So we may notice "attractors" in our system.

As I stated above, Chaos can arrive from simple systems. This is made clear when we look at the equations of motion for the double pendulum.

!!! info "Positions"
	$x_1=l_1\sin \theta _1$
	$y_1=-l_1\cos\theta _1$
	$x_2=x_1+l_2\sin\theta _2 = l_1\sin \theta _1+l_2\sin\theta _2$
	$y_2=y_1-l_2\cos\theta _2 = -l_1\cos\theta _1-l_2\cos\theta _2$

!!! info "Velocities"
	$\dot x_1=l_1\dot \theta_1\cos \theta _1$
	$\dot y_1=l_1\dot \theta_1\sin\theta _1$
	$\dot x_2=\dot x_1+l_2\dot \theta _2\cos\theta _2=l_1\dot \theta _1\cos \theta _1+l_2\dot \theta _2\cos\theta _2$
	$\dot y_2=\dot y_1+l_2\dot \theta _2\sin\theta _2 = l_1\dot \theta _1\sin\theta _1+l_2\dot \theta _2\sin\theta _2$

These look simple but at the same time require us the have a system of 8 equations, as well as there being two unknown quantities, $\dot \theta _1$ and $\dot \theta _2$.

By using the method described in Chaos from Simplicity:An Introduction to the Double Pendulum by Joe Chen$^{2}$, by keeping the kinetic and potential energy of our system fixed, we can "simplify" the system down into 4 equations that describe the rate of change of both angles and the rate of change of both pendulums angular velocity.

!!! warning "System of Equations"
	$w_1=\dot \theta _1$
	$w_2=\dot \theta _2$
	$\dot w_1= \frac{m_2l_1\omega _1^2\sin(2\Delta \theta)+2m_2l_2\omega _2^2\sin\Delta \theta+2gm_2\cos\theta _2 \sin\Delta \theta + 2gm_1\sin\theta _1}{-2l_1(m_1 + m_2\sin ^2\Delta \theta)}$
	$\dot w_1= \frac{m_2l_2\omega _2^2\sin(2\Delta \theta)+2(m_1+m_2)l_1\omega _1^2\sin\Delta \theta+2g(m_1+m_2)\cos\theta _1 \sin\Delta \theta}{2l_2(m_1 + m_2\sin ^2\Delta \theta)}$

!!! help "Quantities"
	$\dot \theta _1,\dot \theta _2$- is the rate of change of the pendulums angle with units $\text{rad} s^{-1}$

	$\Delta \theta$ - is defined as $\theta _1 - \theta _2$ with units $\text{rad}$. 

	$m_1,m_2$ - is the mass at the end of each respective pendulum with units $kg$. 

	$l_1,l_2$ - is the length of the rod attaching each respective pendulum with units $m$. 

	$\omega _1,\omega _2$ - is the angular velocity of each respective pendulum with units $\text{rad}s^{-1}$.

	$\dot \omega _1,\dot \omega _2$ - is the angular acceleration of each respective pendulum with units $\text{rad}s^{-2}$.
	
	$g$ - Gravity with units $ms^{-2}$ ($9.81 ms^{-2}$ at Earth sea level).

Although this looks a lot more complex, a computer will be able to solve this system quickly and efficiently.
"""

# ╔═╡ a3af9025-b852-49b6-af2d-d90304e9ca72
md"""
### Methods

Once again defining; $\Delta \theta = \theta _1 - \theta _2$, we have our system of equations as defined above


!!! warning "System of Equations"
	$w_1=\dot \theta _1$
	$w_2=\dot \theta _2$
	$\dot w_1= \frac{m_2l_1\omega _1^2\sin(2\Delta \theta)+2m_2l_2\omega _2^2\sin\Delta \theta+2gm_2\cos\theta _2 \sin\Delta \theta + 2gm_1\sin\theta _1}{-2l_1(m_1 + m_2\sin ^2\Delta \theta)}$
	$\dot w_1= \frac{m_2l_2\omega _2^2\sin(2\Delta \theta)+2(m_1+m_2)l_1\omega _1^2\sin\Delta \theta+2g(m_1+m_2)\cos\theta _1 \sin\Delta \theta}{2l_2(m_1 + m_2\sin ^2\Delta \theta)}$

We will be constructing a function in `Julia` to define this system. To solve the system we will be using `OrdinaryDiffEq.jl`. The algorithm we will use in tandem with `OrdinaryDiffEq.jl` will be either `Vern6` or `Vern7`, we will decide later on whether we prefer accuracy over efficiency, that being `Vern7` will take longer but be more accurate. Some helpful packages might include `ChaosTools.jl` and `BifurcationKit.jl`.

As a sanity check of our results, we will be testing if the limit as $m_2\rightarrow 0$ turns into a normal single pendulum. Another check we will keep a close eye on is the conservation of energy. We are aware that our system of equations does conserve energy but since we don't have a numerical method that is 100% accurate, we need to be careful that our system doesn't continuously generate or lose energy.

"""

